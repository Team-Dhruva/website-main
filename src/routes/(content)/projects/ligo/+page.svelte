  <h1 class="text-6xl text-center font-bold text-white">LIGO</h1>

  <div class="flex flex-col max-w-screen-lg w-full gap-2">
    <h1 class="text-2xl font-semibold">Goals</h1>
    <p>
      To detect and study gravitational waves, which are ripples in spacetime
      caused by violent and energetic processes in the universe.
    </p>

    <hr class="mt-4 mb-4" />

    <p>
      Gravitational waves provide a new way to observe and understand events
      such as the merging of black holes and neutron stars, supernovae, and
      potentially other astrophysical phenomena.
    </p>
    <p>
      LIGO, an observatory at Caltech, uses a technique called laser
      interferometry to detect gravitational waves. This involves splitting a
      laser beam into two perpendicular arms that stretch several kilometers
      long. These beams are then reflected back and forth between mirrors at the
      ends of the arms.
    </p>
    <p>
      At dhRuVa, we process the raw data collected by LIGO's detectors to filter
      out noise and identify potential gravitational wave signals. We develop
      and apply algorithms to analyze the data, simulate waveforms, and help
      distinguish genuine gravitational wave signals from noise.
    </p>
