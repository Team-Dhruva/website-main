  <h1 class="text-6xl text-center font-bold text-white">NanoGrav</h1>

  <div class="flex flex-col max-w-screen-lg w-full gap-2">
    <h1 class="text-2xl font-semibold">Goals</h1>
    <p>
      To detect low-frequency gravitational waves, which are thought to
      originate from supermassive black hole binaries and potentially other
      cosmic phenomena.
    </p>

    <hr class="mt-4 mb-4" />

    <p>
      Low-frequency gravitational waves are mainly thought to originate from
      supermassive black hole binaries, which are pairs of supermassive black
      holes in close orbit around each other. These waves have much longer
      wavelengths and lower frequencies compared to those detected by LIGO.
    </p>
    <p>
      NanoGrav, an observatory for such waves, uses a network of millisecond
      pulsars, which are highly magnetized, rotating neutron stars that emit
      beams of electromagnetic radiation out of their magnetic poles. As these
      pulsars spin, their beams sweep across Earth, and we observe regular radio
      pulses.
    </p>
    <p>
      By precisely measuring the arrival times of these pulses with a precision
      of a few hundred nanoseconds, we can detect slight variations caused by
      gravitational waves. These variations appear as timing irregularities, or
      noise, in the otherwise highly regular pulses from the pulsars.
    </p>
    <p>
      The data collected from multiple pulsars, which are available on the
      NanoGrav website, are analyzed to identify correlated timing irregularities
      that indicate the presence of low-frequency gravitational waves. We
      participate in the development and refinement of analysis techniques, as
      well as the interpretation of the results.
    </p>
